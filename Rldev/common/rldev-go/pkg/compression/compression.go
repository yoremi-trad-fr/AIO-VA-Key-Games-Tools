// ApplyMask applies the static XOR mask to bytecode data starting at origin.
// This is the basic RealLive "encryption" applied to all SEEN files.
// Equivalent to C's rl_prim_apply_mask.
func ApplyMask(buf *binarray.Buffer, origin int) {
	data := buf.Data
	maskIdx := byte(0)
	for i := origin; i < len(data); i++ {
		data[i] ^= xorMask[maskIdx]
		maskIdx++ // wraps naturally as byte (0-255)
	}
}

// ApplyXORKeys applies per-game XOR encryption keys to decompressed data.
// Equivalent to OCaml's apply_xorkey / C's key-based decryption.
func ApplyXORKeys(buf *binarray.Buffer, keys []gamedef.XORSubkey) {
	data := buf.Data
	size := len(data)

	for _, sk := range keys {
		pos := sk.Offset
		for idx := 0; idx < sk.Length && pos < size; idx++ {
			data[pos] ^= sk.Data[idx%16]
			pos++
		}
	}
}

// Decompress performs RealLive LZ77 decompression.
// src should contain compressed data (starting with 8-byte header: compSize + uncompSize).
// dst should be pre-allocated to the uncompressed size.
// Equivalent to C's rl_prim_decompress.
func Decompress(src, dst []byte) error {
	if len(src) < 9 {
		return fmt.Errorf("compressed data too short: %d bytes", len(src))
	}

	srcIdx := 8 // skip 8-byte size header
	dstIdx := 0
	dstLen := len(dst)
	srcLen := len(src)

	flag := src[srcIdx]
	srcIdx++
	bit := 1

	for srcIdx < srcLen && dstIdx < dstLen {
		if bit == 256 {
			bit = 1
			if srcIdx >= srcLen {
				break
			}
			flag = src[srcIdx]
			srcIdx++
		}

		if flag&byte(bit) != 0 {
			// Literal byte
			if srcIdx >= srcLen || dstIdx >= dstLen {
				break
			}
			dst[dstIdx] = src[srcIdx]
			srcIdx++
			dstIdx++
		} else {
			// Back-reference
			if srcIdx+1 >= srcLen {
				break
			}
			count := int(src[srcIdx])
			srcIdx++
			count += int(src[srcIdx]) << 8
			srcIdx++

			offset := (count >> 4) - 1 + 1 // distance back from current position
			repeatStart := dstIdx - offset
			count = (count & 0x0f) + 2 // match length

			if repeatStart < 0 || repeatStart >= dstIdx {
				return fmt.Errorf("corrupt data: invalid back-reference at src=%d dst=%d (repeat=%d)", srcIdx-2, dstIdx, repeatStart)
			}

			for i := 0; i < count && dstIdx < dstLen; i++ {
				dst[dstIdx] = dst[repeatStart+i]
				dstIdx++
			}
		}

		bit <<= 1
	}

	return nil
}

// --- LZ77 Compressor (port of lzcomp.h C++ template) ---

const (
	rlMaxDist  = 4095
	rlMaxMatch = 17
	rlMinMatch = 2
	hashSize   = 256
	windowSize = 4096
)

// lzMatch stores a match result during compression.
type lzMatch struct {
	pos    int
	length int
}

// Compress performs RealLive LZ77 compression with RealLive-specific bit reversal.
// Returns the compressed data (without the 8-byte size header).
// The caller is responsible for prepending compressedSize + uncompressedSize.
// Equivalent to C's rl_prim_compress.
func Compress(src []byte) []byte {
	srcLen := len(src)
	if srcLen == 0 {
		return nil
	}

	// Output container with bit reversal (RLDataContainer behavior)
	output := make([]byte, 0, srcLen*9/8+256)

	// Process in groups of 8 items (1 flag byte + up to 8 items)
	pos := 0
	for pos < srcLen {
		// Build a temporary group: flag byte + items
		var groupBuf []byte
		flag := byte(0)
		flagMask := byte(0x80)

		groupBuf = append(groupBuf, 0) // placeholder for flag byte

		for i := 0; i < 8 && pos < srcLen; i++ {
			// Try to find a match
			match := findLongestMatch(src, pos, srcLen)

			if match.length >= rlMinMatch {
				// Encode back-reference
				dist := pos - match.pos
				compData := (dist << 4) | (match.length - 2)
				groupBuf = append(groupBuf, byte(compData&0xff), byte(compData>>8))
				pos += match.length
			} else {
				// Literal byte
				flag |= flagMask
				groupBuf = append(groupBuf, src[pos])
				pos++
			}
			flagMask >>= 1
		}

		// Apply reversed flag byte (RealLive-specific)
		groupBuf[0] = reverseBits[flag]
		output = append(output, groupBuf...)
	}

	return output
}

// findLongestMatch searches for the longest matching sequence in the sliding window.
func findLongestMatch(src []byte, pos, srcLen int) lzMatch {
	best := lzMatch{pos: 0, length: 0}

	maxLen := rlMaxMatch
	if pos+maxLen > srcLen {
		maxLen = srcLen - pos
	}
	if maxLen < rlMinMatch {
		return best
	}

	// Search backward in the window
	windowStart := pos - rlMaxDist
	if windowStart < 0 {
		windowStart = 0
	}

	for searchPos := windowStart; searchPos < pos; searchPos++ {
		// Count matching bytes
		matchLen := 0
		for matchLen < maxLen && src[searchPos+matchLen] == src[pos+matchLen] {
			matchLen++
		}

		if matchLen >= rlMinMatch && matchLen > best.length {
			best.pos = searchPos
			best.length = matchLen
			if matchLen == maxLen {
				break // Can't do better
			}
		}
	}

	return best
}
